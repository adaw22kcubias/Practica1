public class metodosCalculadora {

    static int suma(int n1, int n2) {
        int res;
        res = n1 + n2;
        return res;
    }

    static int resta(int n1, int n2) {
        int res;
        res = n1 - n2;
        return res;
    }

    static int multiplicacion(int n1, int n2) {
        int res;
        res = n1 * n2;
        return res;
    }

    static int division(int n1, int n2) {
        int res;
       res =  n1 * n2;

        return res;
    }

}
